import React from "react";

export interface CourseColors {
  primary: string;
  secondary: string;
}

export const getCourseColors = (course?: string): CourseColors => {
  const defaultColors: CourseColors = { primary: "#059669", secondary: "#059669" };
  if (!course) return defaultColors;
  
  const c = course.toLowerCase();
  
  // BIOLOGIA, FISICA, GEOCIÊNCIAS, MATEMÁTICA, QUÍMICA, CIÊNCIAS MÉDICAS (Partially)
  if (
    c.includes("biologia") || 
    c.includes("bioquímica") || 
    c.includes("bioquimica") || 
    c.includes("biotecnologia") || 
    c.includes("química") || 
    c.includes("quimica") ||
    c.includes("física") || 
    c.includes("fisica") || 
    c.includes("geologia") || 
    c.includes("matemática") || 
    c.includes("matematica") ||
    c.includes("ciências do mar") ||
    c.includes("ciencias do mar") ||
    c.includes("meteorologia") ||
    c.includes("oceanografia") ||
    c.includes("biomédicas") ||
    c.includes("biomedicas")
  ) return { primary: "#00BFFF", secondary: "#00BFFF" }; // Azul-Claro

  // MEDICINA
  if (c === "medicina") return { primary: "#FFD700", secondary: "#FFD700" };

  // ENGENHARIAS (Tijolo base)
  if (c.includes("engenharia") || c.includes("ambiente") || c.includes("automação") || c.includes("automacao")) {
    const primary = "#B22222"; // Tijolo
    if (c.includes("eletrotécnica") || c.includes("eletrotecnica")) return { primary, secondary: "#87CEEB" }; // Preta
    if (c.includes("eletrónica") || c.includes("eletronica")) return { primary, secondary: "#87CEEB" }; // Azul-Celeste
    if (c.includes("ambiente")) return { primary, secondary: "#A855F7" }; // Roxo
    if (c.includes("gestão industrial") || c.includes("gestao industrial")) return { primary, secondary: "#4B5563" }; // Cinzento-Escuro
    if (c.includes("aeroespacial")) return { primary, secondary: "#1E3A8A" }; // Azul-Universo
    if (c.includes("computadores e informática") || c.includes("computadores e informatica")) return { primary, secondary: "#1E3A8A" }; // Azul-Escuro
    if (c.includes("informática") || c.includes("informatica")) return { primary, secondary: "#059669" }; // Verde
    if (c.includes("materiais")) return { primary, secondary: "#C41E3A" }; // Vermelho
    if (c.includes("civil")) return { primary, secondary: "#78350F" }; // Castanho
    if (c.includes("automação industrial") || c.includes("automacao industrial")) return { primary, secondary: "#40E0D0" }; // Azul-Turquesa
    if (c.includes("mecânica") || c.includes("mecanica")) return { primary, secondary: "#94A3B8" }; // Cinzento
    if (c.includes("biomédica") || c.includes("biomedica")) return { primary, secondary: "#800000" }; // Bordô
    if (c.includes("física") || c.includes("fisica")) return { primary, secondary: "#FFD700" }; // Amarelo
    if (c.includes("química") || c.includes("quimica")) return { primary, secondary: "#F8F8F8" }; // Branco
    return { primary, secondary: primary };
  }

  // BIOLOGIA E GEOLOGIA
  if (c.includes("biologia e geologia")) return { primary: "#000000", secondary: "#00BFFF" };

  // CIÊNCIAS MÉDICAS (Ciências Biomédicas)
  if (c.includes("ciências biomédicas") || c.includes("ciencias biomedicas")) return { primary: "#00BFFF", secondary: "#FFD700" };

  // ADMINISTRAÇÃO PÚBLICA
  if (c.includes("administração pública") || c.includes("administracao publica")) return { primary: "#000080", secondary: "#FFD700" };

  // COMUNICAÇÃO E ARTE
  const lilas = "#A855F7";
  if (c.includes("design")) return { primary: lilas, secondary: "#C41E3A" }; // Vermelho
  if (c.includes("música") || c.includes("musica")) return { primary: lilas, secondary: "#3B82F6" }; // Azul
  if (c.includes("multimédia") || c.includes("multimedia")) return { primary: lilas, secondary: "#059669" }; // Verde

  // ECONOMIA E GESTÃO
  if (c === "economia") return { primary: "#C41E3A", secondary: "#F8F8F8" }; // Branco
  if (c === "gestão" || c === "gestao") return { primary: "#000000", secondary: "#C41E3A" }; // Vermelho
  if (c.includes("turismo")) return { primary: "#059669", secondary: "#F97316" }; // Laranja
  
  // EDUCAÇÃO E PSICOLOGIA
  if (c.includes("educação básica") || c.includes("educacao basica")) return { primary: "#EC4899", secondary: "#EC4899" };
  if (c.includes("psicologia")) return { primary: "#000000", secondary: "#F97316" }; // Laranja

  // LÍNGUAS
  const navy = "#000080";
  if (c.includes("estudos editoriais")) return { primary: navy, secondary: "#059669" };
  if (c.includes("relações empresariais") || c.includes("relacoes empresariais")) return { primary: navy, secondary: "#C41E3A" };
  if (c.includes("culturas")) return { primary: navy, secondary: "#F97316" };
  if (c.includes("tradução") || c.includes("traducao")) return { primary: navy, secondary: "#F8F8F8" };

  // ESAN
  if (c.includes("automação e sistemas") || c.includes("automacao e sistemas")) return { primary: "#059669", secondary: "#800000" };
  if (c.includes("design de produto")) return { primary: "#059669", secondary: "#00FFFF" }; // Azul Ciano
  if (c.includes("ctesp") && c.includes("aveiro norte")) return { primary: "#059669", secondary: "#BFFF00" }; // Verde Lima

  // ESSUA
  if (c.includes("enfermagem")) return { primary: "#F8F8F8", secondary: "#FFD700" };
  if (c.includes("fisioterapia")) return { primary: "#F8F8F8", secondary: "#000080" };
  if (c.includes("radioterapia")) return { primary: "#F8F8F8", secondary: "#800000" };
  if (c.includes("terapia da fala")) return { primary: "#F8F8F8", secondary: "#A855F7" };
  if (c.includes("ctesp") && c.includes("saúde")) return { primary: "#F8F8F8", secondary: "#BFFF00" };

  // ESTGA
  const gray = "#64748B";
  if (c.includes("eletrónica e mecânica") || c.includes("eletronica e mecanica")) return { primary: gray, secondary: "#000080" };
  if (c.includes("informática aplicada") || c.includes("informatica aplicada")) return { primary: gray, secondary: "#000000" };
  if (c.includes("gestão comercial") || c.includes("gestao comercial")) return { primary: gray, secondary: "#FFC000" }; // Amarelo-Torrado
  if (c.includes("qualidade")) return { primary: gray, secondary: "#F97316" };
  if (c.includes("gestão pública") || c.includes("gestao publica")) return { primary: gray, secondary: "#C41E3A" };
  if (c.includes("comunicação empresarial")) return { primary: gray, secondary: "#800000" };
  if (c.includes("ctesp") && c.includes("águeda")) return { primary: gray, secondary: "#BFFF00" };

  return defaultColors;
};

